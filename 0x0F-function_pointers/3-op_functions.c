#include "3-calc.h"
/**
 * op_add - adds two int
 * @a: first int
 * @b: second int
 * Return: add a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract two int
 * @a: first int
 * @b: second int
 * Return: subtract a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - miltiply two int
 * @a: first int
 * @b: second int
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two int
 * @a: first int
 * @b: second int
 * Return: divides a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns the remainder of division of tw int
 * @a: first int
 * @b: second int
 * Return: remainder of division of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
